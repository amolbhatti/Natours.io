//   console.log(this.pipeline());
//   next();
// });

//Virtual populate
tourSchema.virtual('reviews', {
  ref: 'Review',
  localField: '_id',
  foreignField: 'tour',
});

//VIRTUAL FIELD
tourSchema.virtual('weeksDuration').get(function () {
  return this.duration / 7;
});

module.exports = mongoose.model('Tour', tourSchema); //model export
